# 1. Counts 0 to 49 (by 1)
print("---1. 0 to 50 increments of 1 ---")
for i in range (0, 50):
    print(i)

# 2. Counts 50 down to 0 (by -1)
print("---2. 50 to 0 decrements of -1 ---") 
for i in range (50, -1, -1):
    print (i)   

# 3. Counts 30 to 49 (by 1)
print("---3. 30 to 50 in increments of 1 ---")
for i in range (30, 50):
    print(i)

# 4. Counts 50 down to 10 (by -2)
print("---4. 50 to 10 in decrements of 2")    
for i in range (50, 8, -2):
    print (i)

# 5. Counts 100 to 200 (by 5)
print ("---from 100 to 200 in increments of 5")    
for i in range (100, 205, 5):
    print (i)